    tasks_text = "ğŸ“‹ Ğ’Ğ°ÑˆĞ¸ Ğ·Ğ°Ğ´Ğ°Ñ‡Ğ¸:\n\n"
    for task in tasks:
        status = "âœ…" if task.completed else "â³"
